/**
 * Get remedy for a planet in a specific rashi
 * @param {string} planetName - "Sun", "Moon", etc.
 * @param {string} rashiName - "Aries", "Taurus", etc.
 * @param {string} category - "Donation" or "Temple"
 * @returns {string|null} - Remedy text or null
 */
export const getRemedy = (planetName, rashiName, category) => {
    const remedy = REMEDIES.find(
        r => r.planet === planetName &&
            r.sign === rashiName &&
            r.category === category
    );
    return remedy ? remedy.remedy : null;
};
